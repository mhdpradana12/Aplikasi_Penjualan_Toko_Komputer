    // Method Menampilkan Data Ke Table
    public final void loadData() {
        // Enable Dan Disable Beberapa Button
        btnSimpan.setEnabled(true);
        btnHapus.setEnabled(false);
        btnEdit.setEnabled(false);
        tfKodeBarang.setEnabled(false);
        // Mendapatkan Data
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();

        try {
            //Koneksi Ke Database
            Connection c = koneksi.getKoneksi();
            // Kueri Menampilkan Table Barang
            Statement s = c.createStatement();
            String sql = "SELECT * FROM tbl_barang";
            ResultSet r = s.executeQuery(sql);

            while (r.next()) {
                // Menampilkan Data Table Ke Baris Kolom
                Object[] o = new Object[5];
                o[0] = r.getString("kode_barang");
                o[1] = r.getString("nama_barang");
                o[2] = r.getString("vendor_barang");
                o[3] = r.getString("stok_barang");
                o[4] = r.getString("harga_barang");
                model.addRow(o);
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
    
