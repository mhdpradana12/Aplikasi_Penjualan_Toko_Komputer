    // Method Untuk Menambahkan Data
    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        // Validasi Data Biar Tidak Kosong
        if(tfKodeBarang.getText().equals("") ||tfNamaBarang.getText().equals("") ||tfVendorBarang.getText().equals("") ||tfStokBarang.getText().equals("") ||tfHargaBarang.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Mohon Lengkapi Data||!", "Pesan", JOptionPane.INFORMATION_MESSAGE);
        }else{
            // Menyimpan String Dari Text Field
            String kbarang = tfKodeBarang.getText();
            String nbarang = tfNamaBarang.getText();
            String vbarang = tfVendorBarang.getText();
            String sbarang = tfStokBarang.getText();
            String hbarang = tfHargaBarang.getText();
           
            try {
                // Mengambil Data Waktu Sekarang
                long millis=System.currentTimeMillis();  
                java.sql.Date date=new java.sql.Date(millis);  
                System.out.println(date); 
                String tgl = date.toString();
                // Koneksi Ke Database
                Connection c = koneksi.getKoneksi();
                // Kueri Untuk Menambahkan Data
                String sql = "INSERT INTO tbl_barang VALUES (?, ?, ?, ?, ?, ?, ?)";
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, kbarang);
                p.setString(2, nbarang);
                p.setString(3, vbarang);
                p.setString(4, sbarang);
                p.setString(5, hbarang);
                p.setString(6, tgl);
                // Mengambil ID User dari Sesi
                p.setString(7, id_user);
                p.executeUpdate();
                p.close();
            } catch (SQLException e) {
                System.out.println(e.getMessage());
            } finally {
                loadData();
                Clear();
                kode();
                JOptionPane.showMessageDialog(null, "Data berhasil tersimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

