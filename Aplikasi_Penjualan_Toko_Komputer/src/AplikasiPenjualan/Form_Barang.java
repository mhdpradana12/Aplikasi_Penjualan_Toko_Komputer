    // Method Membuat Kode Barang Secara Beraturan
    private void kode() {
        try {
            // Koneksi Ke Database
            Connection c = koneksi.getKoneksi();
            // Kueri Menampilkan Kode Barang Di Table Barang
            Statement s = c.createStatement();
            String sql = "SELECT * FROM tbl_barang ORDER BY kode_barang DESC";
            ResultSet r = s.executeQuery(sql);
            // Validasi Cek Nomor Secara Beraturan
            if (r.next()) {
                String noKode = r.getString("kode_barang");
                String AN = "" + (Integer.parseInt(noKode) + 1);
                tfKodeBarang.setText(AN);
            } else {
                tfKodeBarang.setText("1");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
     
